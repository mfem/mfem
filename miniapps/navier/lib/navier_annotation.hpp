#else

#define NAVIER_PERF_FUNCTION
#define NAVIER_PERF_BEGIN(s)
#define NAVIER_PERF_END(s)
#define NAVIER_PERF_SCOPE(name)
#define NAVIER_PERF_LOOP_BEGIN(loop_id, s)
#define NAVIER_PERF_LOOP_END(loop_id)
#define NAVIER_PERF_LOOP_ITERATION(loop_id, iter)
#define NAVIER_PERF_FUNCTION_BEGIN
#define NAVIER_PERF_FUNCTION_END

#endif //#ifdef NAVIER_USE_CALIPER

#endif //NAVIER_ANNOTATION_HPP

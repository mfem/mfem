void Scale(std::vector<double>& x, std::vector<double>& w)
{
   const int n = x.size();
   for (int i = 0 ; i < n ; ++i)
   {
      x[i] = (1. + x[i])/2.;
      w[i] /= 2.;
   }
   return;
}


